    public void get_data() {
        jProgressBar1.setString("Loading...Please wait...");
        jProgressBar1.setIndeterminate(true);
        Thread t = new Thread(new Runnable() {

            @Override
            public void run() {
                List<Srpt_receipt.field> fields = new ArrayList();
                int total_qty = 0;
                double vatable_sale = 0;
                double zero_rate_sale = 0;
                double vat_exempt_sale = 0;
                double vat_percent = 0;
                for (S1_sales_items.to_sales_items t : datas) {
                    Srpt_receipt.field tbar = new Srpt_receipt.field(t.description, new Date(), t.product_qty * t.selling_price, t.product_qty, t.selling_price);
                    fields.add(tbar);
                    total_qty += t.product_qty;

                }
                vat_percent = sales.amount_due * .12;
                vatable_sale = sales.amount_due - vat_percent;
                String business_name = jTextField1.getText();
                String operated_by = jTextField2.getText();
                String address = jTextField3.getText();
                String tin_no = "Tin No: " + jTextField4.getText();
                String serial_no = "Serial No: " + jTextField5.getText();
                String permit_no = "Permit No: " + jTextField6.getText();
                String datetime = DateType.slash_w_time.format(new Date());
                String or_no = System.getProperty("or_no", "OR NO. 000000000001");
                String items = System.getProperty("items", "");
                double total_due = sales.amount_due;
                double cash = sales.amount_due - sales.check_amount;
                double change_due = sales.amount_paid - sales.amount_due;
                String receipt_infos = System.getProperty("receipt_infos", "");
                String receipt_footer = System.getProperty("receipt_footer", "This is an Official receipt");
                double discount = sales.discount_amount;
                String customer_name = sales.customer_names;
                String cashier_name = sales.user_name;
                String discount_name = sales.discount_name;
                String pos_no = "POS 1";

                String accreditation_no = "";
                String trans_no = "";
                String check_bank = "bank";
                double check_amount = 0;
                String telephone_number = "";
                String discount_customer_name = "";
                String discount_customer_id = "";
                String min_no = "";
                double addtl_service = 0;
                Srpt_receipt rpt = new Srpt_receipt(business_name, operated_by, address, tin_no, serial_no, permit_no, datetime, or_no, items, total_due, cash, change_due, receipt_infos, receipt_footer, discount, customer_name, cashier_name, discount_name, pos_no, total_qty, vatable_sale, zero_rate_sale, vat_exempt_sale, vat_percent, accreditation_no, trans_no, check_bank, check_amount, telephone_number, discount_customer_name, discount_customer_id, min_no, addtl_service);
                rpt.fields.addAll(fields);
                String jrxml = "rpt_receipt.jrxml";
                report_sales_items(rpt, jrxml);
                jProgressBar1.setString("Finished...");
                jProgressBar1.setIndeterminate(false);
            }
        });
        t.start();

    }

    private void report_sales_items(final Srpt_receipt to, String jrxml_name) {
        pnl_report.removeAll();
        pnl_report.setLayout(new BorderLayout());
        try {
            JRViewer viewer = get_viewer_all_sales(to, jrxml_name);
            JPanel pnl = new JPanel();
            pnl.add(viewer);
            pnl.setVisible(true);
            pnl.setVisible(true);
            pnl_report.add(viewer);
            pnl_report.updateUI();
        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

    public static JRViewer get_viewer_all_sales(Srpt_receipt to, String rpt_name) {
//        Connection con11 = null;
        try {
            return JasperUtil.getJasperViewer(
                    compileJasper(rpt_name),
                    JasperUtil.setParameter(to),
                    JasperUtil.makeDatasource(to.fields));
        } catch (Exception e) {
            throw new RuntimeException(e);
        } finally {
        }
    }

    public static JasperReport compileJasper(String rpt_name) {
        try {
            String jrxml = rpt_name;
            InputStream is = Srpt_receipt.class.getResourceAsStream(jrxml);
            JasperReport jasper = JasperCompileManager.compileReport(is);
            return jasper;
        } catch (JRException e) {
            throw new RuntimeException(e);
        }
    }
